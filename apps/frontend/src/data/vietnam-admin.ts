export function getProvinceNames(): string[] {
  return VIETNAM_PROVINCES.map((p) => p.name);
}

export function getDistricts(provinceName: string): District[] {
  return VIETNAM_PROVINCES.find((p) => p.name === provinceName)?.districts ?? [];
}

export function getWards(provinceName: string, districtName: string): Ward[] {
  const districts = getDistricts(provinceName);
  return districts.find((d) => d.name === districtName)?.wards ?? [];
}

export function getAllWardsInProvince(provinceName: string): Ward[] {
  const province = VIETNAM_PROVINCES.find((p) => p.name === provinceName);
  if (!province) return [];
  const seen = new Set<string>();
  const result: Ward[] = [];
  for (const d of province.districts) {
    for (const w of d.wards) {
      if (!seen.has(w.name)) {
        seen.add(w.name);
        result.push(w);
      }
    }
  }
  return result;
}